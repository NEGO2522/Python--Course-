# Type Casting in Python 

# Convert String to integer 
num_str = "245"
num_int = int(num_str)  # Type casting with int() built in function.
print(type(num_int));
# Convert integer to string.
num = 25;
num_str = str(num);
print(type(num_str)); # Type casting with str() built in function.
# Convert float to integer
pi = 3.14;
pi_int = int(pi); # Type Casting with float() built in function. 
print(type(pi_int));