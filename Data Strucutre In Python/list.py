# List in Python
marks = [54,23,64,94,32] # List in python are denote with these [] square brackets.
mixed = [43,"Kshitij",False,4.2]
print(marks)
print(mixed)
 # Index always start from zero no matter it is list, tupple or set or dictionary.
print(marks[0]) # 54
print(mixed[2]) # False
print(mixed[4]) # list index Out of range.
# Slicing In list 
print(marks[0:2]) # 54,23
print(mixed[1:3]) # "Kshitij", False

